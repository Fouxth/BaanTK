// API Configuration
const ID_CARD_API_CONFIG = {
  BASE_URL: "https://www.programmerthailand.com/services/idcard-check",
  TIMEOUT: 10000,
  RETRY_COUNT: 3
};

/**
 * ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡∏Ç‡∏≠‡∏á‡πÄ‡∏•‡∏Ç‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô‡πÑ‡∏ó‡∏¢‡∏ú‡πà‡∏≤‡∏ô API
 * @param {string} idCard - ‡πÄ‡∏•‡∏Ç‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô 13 ‡∏´‡∏•‡∏±‡∏Å
 * @returns {Promise<Object>} ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö
 */
async function validateThaiIDCard(idCard) {
  try {
    console.log(`üîç Validating Thai ID Card via API: ${idCard ? idCard.substring(0, 4) + "****" : "null"}`);

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏¢‡∏≤‡∏ß‡πÅ‡∏•‡∏∞‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡πÄ‡∏ö‡∏∑‡πâ‡∏≠‡∏á‡∏ï‡πâ‡∏ô
    if (!idCard || idCard.length !== 13) {
      console.log(`‚ùå Invalid length: ${idCard ? idCard.length : 0} (should be 13)`);
      return {
        isValid: false,
        error: "‡πÄ‡∏•‡∏Ç‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô‡∏ï‡πâ‡∏≠‡∏á‡∏°‡∏µ 13 ‡∏´‡∏•‡∏±‡∏Å"
      };
    }

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
    if (!/^\d{13}$/.test(idCard)) {
      console.log(`‚ùå Invalid format: contains non-numeric characters`);
      return {
        isValid: false,
        error: "‡πÄ‡∏•‡∏Ç‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô"
      };
    }

    // ‡πÄ‡∏£‡∏µ‡∏¢‡∏Å API ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö
    const apiResult = await callIDCardAPI(idCard);

    if (apiResult.success) {
      console.log(`‚úÖ API validation successful`);
      return {
        isValid: true,
        data: apiResult.data,
        source: "external_api"
      };
    } else {
      console.log(`‚ùå API validation failed: ${apiResult.error}`);
      // ‡∏ñ‡πâ‡∏≤ API ‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß ‡πÉ‡∏´‡πâ fallback ‡πÑ‡∏õ‡πÉ‡∏ä‡πâ‡∏Å‡∏≤‡∏£‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö checksum
      const checksumResult = validateChecksumOnly(idCard);
      return {
        isValid: checksumResult,
        data: checksumResult ? { status: "checksum_valid" } : null,
        source: "checksum_fallback",
        warning: `API unavailable: ${apiResult.error}`
      };
    }
  } catch (error) {
    console.error(`‚ùå Error validating ID Card:`, error.message);
    // Fallback to checksum validation
    const checksumResult = validateChecksumOnly(idCard);
    return {
      isValid: checksumResult,
      data: checksumResult ? { status: "checksum_valid" } : null,
      source: "checksum_fallback",
      error: error.message
    };
  }
}

/**
 * ‡πÄ‡∏£‡∏µ‡∏¢‡∏Å API ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ö‡∏±‡∏ï‡∏£‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏ä‡∏ô
 */
async function callIDCardAPI(idCard) {
  let lastError;

  for (let attempt = 1; attempt <= ID_CARD_API_CONFIG.RETRY_COUNT; attempt++) {
    try {
      console.log(`üì° Calling ID Card API (attempt ${attempt}/${ID_CARD_API_CONFIG.RETRY_COUNT})`);

      const response = await axios.get(ID_CARD_API_CONFIG.BASE_URL, {
        params: {
          id: idCard
        },
        timeout: ID_CARD_API_CONFIG.TIMEOUT,
        headers: {
          "User-Agent": "BaanTK-LoanSystem/1.0",
          "Accept": "application/json"
        }
      });

      if (response.data) {
        return {
          success: true,
          data: {
            idCard: idCard,
            valid: response.data.valid || response.data.status === "valid",
            message: response.data.message || "Valid ID Card",
            details: response.data
          }
        };
      } else {
        throw new Error("Invalid API response format");
      }
    } catch (error) {
      lastError = error;
      console.warn(`‚ö†Ô∏è API call attempt ${attempt} failed:`, error.message);

      if (attempt < ID_CARD_API_CONFIG.RETRY_COUNT) {
        // Wait before retry (exponential backoff)
        await new Promise((resolve) => setTimeout(resolve, 1000 * attempt));
      }
    }
  }

  return {
    success: false,
    error: lastError?.message || "API call failed after all retries"
  };
}

/**
 * ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö checksum ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô (‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö fallback)
 */
function validateChecksumOnly(idCard) {
  try {
    let sum = 0;
    for (let i = 0; i < 12; i++) {
      sum += parseInt(idCard[i]) * (13 - i);
    }

    const remainder = sum % 11;
    const checkDigit = (11 - remainder) % 10;
    const lastDigit = parseInt(idCard[12]);

    return checkDigit === lastDigit;
  } catch (error) {
    return false;
  }
}

